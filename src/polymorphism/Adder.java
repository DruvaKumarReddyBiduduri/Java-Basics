package polymorphism;

public class Adder {
    static  int add(int a,int b){
        return a+b;
    }

    static double add(double a,double b){
        return a+b;
    }

    static float add(float a,float b){
        return a+b;
    }

    static float add(long a,long b){
        return a+b;
    }
}
